def get_guidance_by_number(number: str) -> dict | None:
    """根据控制项编号获取 ISO 27002 实施指南"""
    return ISO27002_GUIDANCE.get(number.upper())


def get_guidance_summary(number: str) -> str:
    """生成指定控制项的 27002 指南摘要，用于注入 LLM 提示词"""
    item = get_guidance_by_number(number)
    if not item:
        return ""
    lines = [f"### ISO/IEC 27002:2022 控制项 {number} {item['title']}",
             f"**控制目的：** {item['purpose']}",
             "**实施指南：**"]
    for g in item["guidance"]:
        lines.append(f"- {g}")
    lines.append("**审核关注点：**")
    for a in item["audit_points"]:
        lines.append(f"- {a}")
    return "\n".join(lines)


def get_all_guidance_numbers() -> list[str]:
    """获取所有 27002 控制项编号"""
    return list(ISO27002_GUIDANCE.keys())
